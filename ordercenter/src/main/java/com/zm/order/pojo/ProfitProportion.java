package com.zm.order.pojo;

public class ProfitProportion {

	private Integer id;
	
	private Double total_profit_proportion;
	
	private Double register_profit_proportion;
	
	private Double consume_profit_proportion;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Double getTotal_profit_proportion() {
		return total_profit_proportion;
	}

	public void setTotal_profit_proportion(Double total_profit_proportion) {
		this.total_profit_proportion = total_profit_proportion;
	}

	public Double getRegister_profit_proportion() {
		return register_profit_proportion;
	}

	public void setRegister_profit_proportion(Double register_profit_proportion) {
		this.register_profit_proportion = register_profit_proportion;
	}

	public Double getConsume_profit_proportion() {
		return consume_profit_proportion;
	}

	public void setConsume_profit_proportion(Double consume_profit_proportion) {
		this.consume_profit_proportion = consume_profit_proportion;
	}
	
}
